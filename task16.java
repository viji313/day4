package day4;

public class task16 {
	public static void main(String[]args)
	{
		String month = "jan";
		switch(month)
		{
		case "jan" :
			System.out.println("1");
			break;
			
		case "feb":
			System.out.println("2");	
			break;
			
		case "mar" :
			System.out.println("3");
			break;
			
		case "apr" :
			System.out.println("4");
			break;
			
		case "may" :
			System.out.println("5");
			break;
			
			default :
				System.out.println("not valid");
				break;
	}

}
}
